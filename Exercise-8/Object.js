// Objects in JavaScript

// Car object with properties like make, model, year...!


let car = {
    make: 'China',
    model: 'Toyota',
    year: 2020,
};

// Accessing the properties of the car object
console.log(car.make);
console.log(car.model);
console.log(car.year);

// Accessing the all properties of the car object
console.log(car);

