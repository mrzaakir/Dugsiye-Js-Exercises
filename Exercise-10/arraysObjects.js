// Arrays inside Objects in javaScript Exercise 

//  Library object with properties like title, author, year...!

let libary = [
    {
        title: "48 Laws of Power",
        author: "Robert Greene",
        year: 1998
    },
    {
        title: 'The Great Gatsby',
        author: 'F. Scott Fitzgerald',
        year: 1925
    }
]

// the original array of objects
console.log(libary);

console.log(libary[0].title)
console.log(libary[1].author)
