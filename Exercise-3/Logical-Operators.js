// Logical Operators in JavaScript

let isSunny = false;
let isWeekend = true;


// Logical AND (&&)
console.log(`Logical AND (&&): ${isSunny && isWeekend}`); 

console.log('   ');

// Logical OR (||)
console.log(`Logical OR (||): ${isSunny || isWeekend}`); 

console.log('   ');

// Logical NOT (!)
console.log(`Logical NOT (!): ${!isSunny}`); 

console.log('   ');

console.log(`Logical NOT (!): ${!isWeekend}`); 


