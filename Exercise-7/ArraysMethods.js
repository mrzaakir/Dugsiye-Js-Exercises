// Arrays Methods in javaScript


// Using Methods of array like push, shift, then Loging The Array

let colors = ['red', 'blue', 'green', 'yellow', 'orange'];
console.log(colors);

// before using methods
console.log('Before using methods:');
console.log(colors);


// Using push method to add a new color to the end of the array
colors.push('gray');
console.log('After using push method:');
console.log(colors);

// Using shift method to remove the first color from the array
colors.shift();
console.log('After using shift method:');
console.log(colors);

